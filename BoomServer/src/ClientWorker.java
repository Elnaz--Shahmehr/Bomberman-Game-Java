import java.io.*;
/**
 * Holds the PrintWriter which will be used with collection of outgoing connections 
 * to send data to clients.
 * 
 */
class ClientWorker {
	
	PrintWriter out;
	
	ClientWorker(){
		this.out = null;
	}
	
	ClientWorker(PrintWriter out){
		this.out = out;
	}
}
